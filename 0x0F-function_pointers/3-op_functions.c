#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - add numbers
 * @a: num1
 * @b: num2
 * Return: number
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub numbers
 * @a: num1
 * @b: num2
 * Return: number
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - add numbers
 * @a: num1
 * @b: num2
 * Return: number
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - add numbers
 * @a: num1
 * @b: num2
 * Return: number
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add numbers
 * @a: num1
 * @b: num2
 * Return: number
 */
int op_mod(int a, int b)
{
	return (a % b);
}
